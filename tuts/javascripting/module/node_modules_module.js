var fs = require('fs')  
var path = require('path')  


module.exports = function (dirname, dirext, callback) {

	fs.readdir(dirname, function (err, list) {  
		if (err) {
			return callback(err);
		};

		data = [];

		list.forEach(function (file) {  
			if (path.extname(file) === '.' + dirext)  {
				data.push(file);
			};
		});

		callback(null, data);

	});  
};





